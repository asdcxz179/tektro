    {
        $support = SupportFile::where('file_name',$id)->first();
        if(!$support) {
            $brand = ProductBrand::where('file_data_name',$id)->first();
            $file = $brand->file;
        }else {
            $file = $support->path;
        }
        return response()->file(public_path($file));
    }

