    {
        $data['product'] = Product::where(['status'=>1,'id'=>$id])->first();
        $category = $data['product']->category;
        $data['category'] = $category?$category->first():[];
        $data['brand'] = ($data['category'])?$data['category']->brands->first():'';
        return view('front.product',$data);
